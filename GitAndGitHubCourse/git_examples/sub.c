#include <stdio.h>

int main()
{
    // Declare and initialize two integer variables
    int a = 200;
    int b = 130;

    // Perform subtraction
    int sub = a - b;

    // Display the result
    printf("Subtraction Result = %d\n", sub);

    return 0;
}
